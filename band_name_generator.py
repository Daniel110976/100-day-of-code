# Band Name Generator
# This program generates a band name based on user input
# It combines the city, pet, country, and favorite color to create a unique band name
print("Welcome to the Band Name Generator!")
city = input("What's the name of the city you grew up in?")
pet = input("What's the name of your pet?")
country = input("what's the name of your country?")
favorite_color = input("What's your favorite color?")
print("Your band name could be " + city + " " + pet + " ")
print("Your band name could be " + country + " " + favorite_color + " ")
print(city + " " + country)